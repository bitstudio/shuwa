const DOUBLE_NAME = [
    'Hksl_bicycle',
    'Jsl_bicycle',
    'Hksl_hat',
    'Jsl_cap',
    'Hksl_hearing_aid',
    'Jsl_hearing_aid',
    'Hksl_motorcycle',
    'Jsl_motorcycle',
    'Hksl_winter',
    'Jsl_winter',
];

export const CheckDouble = (input: string) => {
    let index = 0;
    // check jsl or hksl
    const lang = input.split('_');
    for (const label of DOUBLE_NAME) {
        if (input === label) {
            if (lang[0] === 'Hksl')
                return DOUBLE_NAME[index] + '-' + DOUBLE_NAME[index + 1];
            else return DOUBLE_NAME[index - 1] + '-' + DOUBLE_NAME[index];
        }
        index += 1;
    }
    return false;
};
